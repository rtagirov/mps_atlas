      subroutine oparcoe(f, x, a, b, c, n)
*
*.... 1995 OCT - MAKE THE DUMMY VARIABLES ASSUMED SIZE

*
      implicit none
      include 'common.sizebl'

      double precision f(*), x(*), a(*), b(*), c(*)        
      double precision d, wt
      integer j, j1, n, n1

      c(1)=0.                                                                   
      b(1)=(f(2)-f(1))/(x(2)-x(1))                                              
      a(1)=f(1)-x(1)*b(1)                                                       
      n1=n-1                                                                    
      c(n)=0.                                                                   
      b(n)=(f(n)-f(n1))/(x(n)-x(n1))                                            
      a(n)=f(n)-x(n)*b(n)                                                       
      if(n.eq.2)return                                                          

      do  j=2,n1                                                               
      j1=j-1                                                                    
      d=(f(j)-f(j1))/(x(j)-x(j1))                                               
      c(j)=f(j+1)/((x(j+1)-x(j))*(x(j+1)-x(j1)))-f(j)/((x(j)-x(j1))*            
     &(x(j+1)-x(j)))+f(j1)/((x(j)-x(j1))*(x(j+1)-x(j1)))                        
      b(j)=d-(x(j)+x(j1))*c(j)                                                  
      a(j)=f(j1)-x(j1)*d+x(j)*x(j1)*c(j)                                        
      end do

      c(2)=0.                                                                   
      b(2)=(f(3)-f(2))/(x(3)-x(2))                                              
      a(2)=f(2)-x(2)*b(2)                                                       
      c(3)=0.                                                                   
      b(3)=(f(4)-f(3))/(x(4)-x(3))                                              
      a(3)=f(3)-x(3)*b(3)                                                       

      do  j=2,n1                                                               
       if(c(j).ne.0.) then                                                      
        j1=j+1                                                                    
        wt=abs(c(j1))/(abs(c(j1))+abs(c(j)))                                      
        a(j)=a(j1)+wt*(a(j)-a(j1))                                                
        b(j)=b(j1)+wt*(b(j)-b(j1))                                                
        c(j)=c(j1)+wt*(c(j)-c(j1))                                                
       end if
      end do 
      a(n1)=a(n)                                                                
      b(n1)=b(n)                                                                
      c(n1)=c(n)                                                                
      return                                                                    
      end                                                                       
